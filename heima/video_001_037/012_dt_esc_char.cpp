#include<iostream>
using namespace std;


int main() {
    
    // 转义字符
    // 换行符 \n
    cout << "hello world";
    cout << "hello world" << endl;
    cout << "hello world\n";

    // \t 水平制表符，可以让输出看着更整齐
    cout << "aaa\thello" << endl;
    cout << "aa\thello" << endl;
    cout << "aaaaa\thello" << endl;



    return 0;
}